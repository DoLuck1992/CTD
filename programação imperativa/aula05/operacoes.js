//adição
console.log(2+2);

//subtração
console.log(10-5);

//multiplicacao
console.log(10*5);

//divisao
console.log(10/2);

//incremento
let numero = 10;
console.log(numero++);//Fez o incremento após a impressão
console.log(++numero);

//decremento
console.log(--numero);

//comparação de maior
console.log(10>5);

//comparação de menor
console.log(10<5);

//comparação de menor ou igual
console.log(3<=3);

//comparacao de maior ou igual
console.log(20 >= 20);

//comparação de igualdade
console.log(10==10);

//comparação de igualdade rígido
console.log(5 === '5');

//comparação de desigualdade
console.log(10!=10);

//comparação de desigualdade  rígido
console.log(10!=="10");

//comparador lógico &&
console.log(10>5 && 8<10);

//comparador lógico ||
console.log(10>5 || 8>10);

//comparador de negação !
console.log(true);
console.log(!true);

//concatenação
let nome = "João"
let sobrenome = "Oliveira"
console.log(nome + " " + sobrenome);